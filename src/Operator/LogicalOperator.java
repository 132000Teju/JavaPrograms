package Operator;

public class LogicalOperator {
    public static void main (String[] args) {
        // Logical operators
        boolean x = true;
        boolean y = false;
        // and logical operator
        System.out.println("x && y: " + (x && y));
        // or logical operator
        System.out.println("x || y: " + (x || y));
        // not logical operator
        System.out.println("!x: " + (!x));
    }
}

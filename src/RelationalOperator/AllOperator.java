package RelationalOperator;

public class AllOperator {
    public static void main(String[] args)
    {
        int a = 10;
        int b = 3;
        int c = 5;
        //greater than
        System.out.println("a > b: " + (a > b));
        //less than
        System.out.println("a < b: " + (a < b));
        //greater than equal to
        System.out.println("a >= b: " + (a >= b));
        //less than equal to
        System.out.println("a <= b: " + (a <= b));
        //equal to
        System.out.println("a == c: " + (a == c));
        //not equal to
        System.out.println("a != c: " + (a != c));
    }
}

package UnaryOperator;

public class Combination {
    public static void main(String[] args){
        int a=10;
        int b=20;
        //pre-post increment
        System.out.println(a++);
        System.out.println(++a);
        //pre-post decrement
        System.out.println(b--);
        System.out.println(--b);
 }
}
